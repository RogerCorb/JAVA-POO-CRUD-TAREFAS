    private void jLabelTollBarSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelTollBarSaveMouseClicked
        // TODO add your handling code here:
        // Criamos o evento que salva no banco de dados 
        // o novo projeto , pegando o nome e a descrição dele 
        //que o usuário digitou , após ele ter apertado 
        // o cli do mouse na label CHECK de salvar os dados
        try {
           Project project = new Project();
           project.setName(jTextFieldName.getText());
           project.setDescripition(jTextAreaDescripition.getText());              
           //project.
           Controller.save(project);
           JOptionPane.showMessageDialog(rootPane, "Projeto Salvo com sucesso");
        } catch (Exception e) { 
           JOptionPane.showMessageDialog(rootPane, e.getMessage() );
            
        }
        this.dispose();
        
    }//GEN-LAST:event_jLabelTollBarSaveMouseClicked

